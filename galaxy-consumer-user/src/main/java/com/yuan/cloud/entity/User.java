package com.yuan.cloud.entity;

import java.math.BigDecimal;

public class User {
  private Long id;

  private String name;

  private Short age;

  public Long getId() {
    return this.id;
  }

  public void setId(Long id) {
    this.id = id;
  }

  public String getName() {
    return this.name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public Short getAge() {
    return this.age;
  }

  public void setAge(Short age) {
    this.age = age;
  }
}
